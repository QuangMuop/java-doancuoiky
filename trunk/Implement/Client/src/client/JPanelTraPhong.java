    private void initData()
    {
        WorkerTraPhong worker = new WorkerTraPhong();
        worker.execute();

        try {
            lstThuePhong = worker.get();
        } catch (InterruptedException ex) {
            Logger.getLogger(JPanelTraPhong.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExecutionException ex) {
            Logger.getLogger(JPanelTraPhong.class.getName()).log(Level.SEVERE, null, ex);
        }

        if(lstThuePhong!=null && lstThuePhong.size()>0)
        {
            initComboBoxMaPhong();
            hienThiThongTinThuePhong(lstThuePhong.get(0));

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            if(sdf.format(lstThuePhong.get(0).getNgayThue()).equals(sdf.format(MyDateTime.getNow())))
            {
                jBtnHuyDatPhong.setEnabled(true);
            }
            else
            {
                jBtnHuyDatPhong.setEnabled(false);
            }
            jBtnTraPhong.setEnabled(true);
        }
        else
        {
            jBtnTraPhong.setEnabled(false);
            jBtnHuyDatPhong.setEnabled(false);
            clearData();
        }
    }

    private void hienThiThongTinThuePhong(ThuePhong thuePhong) {
        Calendar calendar = Calendar.getInstance();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                
        Phong phong = thuePhong.getPhong();
        int gia = phong.getGia() + phong.getDonGiaPhong();//.getIdLoaiPhong().getGia();
        jLabelDonGia.setText("" + gia);
        
        jLabelLoaiPhong.setText(phong.getTenLoaiPhong());//.getIdLoaiPhong().getTen());

        Date ngayTra = calendar.getTime();
        jLabelNgayTra.setText(sdf.format(ngayTra));
        
        Date ngayThue = thuePhong.getNgayThue();
        jLabelNgayThue.setText(sdf.format(ngayThue));
                
        int soNgayThue = MyDateTime.SubDate(ngayThue, ngayTra) + 1;
        jLabelSoNgayThue.setText("" + soNgayThue);        

        jLabelTongTien.setText("" + thuePhongController.tinhTienThuePhong(thuePhong, soNgayThue));

        //hien thi thong tin tren table
        ArrayList<KhachHang> arrKhach = thuePhong.getLstKhachHang();

        jLabelSoNguoiCuTru.setText("" + arrKhach.size());

        fillTableKhachHang(arrKhach);
    }

    private void fillTableKhachHang(ArrayList<KhachHang> arrKhach)
    {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        jTableKhachHang.setModel(new javax.swing.table.DefaultTableModel(
            header,0
        ) {
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.Integer.class
            };
            boolean[] canEdit = new boolean [] {
                false, false, false, false, false, false, false
            };

            @Override
            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });

        DefaultTableModel model = (DefaultTableModel) jTableKhachHang.getModel();

        int i;
        for(i=0;i<arrKhach.size();i++)
        {
            KhachHang khach = arrKhach.get(i);

            Object[] arr = new Object[7];

            arr[0] = khach.getTen();

            Date ngaySinh = khach.getNgaySinh();
            if(ngaySinh!=null)
                arr[1] = sdf.format(ngaySinh);
            else
                arr[1] = "";

            arr[2] = khach.getGioiTinh();
            arr[3] = khach.getDiaChi();
            arr[4] = khach.getDienThoai();
            arr[5] = khach.getTenLoaiKhachHang();//.getIdLoaiKhachHang().getTen();
            arr[6] = khach.getDonGiaKhachHang();//.getIdLoaiKhachHang().getGia();

            model.addRow(arr);

        }
    }

