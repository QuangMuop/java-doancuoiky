    private ThuePhong timThuePhongTheoMaPhong(int id)
    {
        int i;
        for(i=0;i<lstThuePhong.size();i++)
        {
            if(lstThuePhong.get(i).getPhong().getId() == id)
                return lstThuePhong.get(i);
        }
        return null;
    }

    private void jBtnTraPhongMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnTraPhongMousePressed
        // TODO add your handling code here:
        try
        {
            if(!this.jBtnTraPhong.isEnabled())
                return;

            ThuePhong thuePhong = timThuePhongTheoMaPhong(Integer.parseInt(jCbMaPhong.getSelectedItem().toString()));

            Calendar calendar = Calendar.getInstance();

            Date ngayTra = calendar.getTime();
            thuePhong.setNgayTra(ngayTra);

            Date ngayThue = thuePhong.getNgayThue();

            int soNgayThue = MyDateTime.SubDate(ngayThue, ngayTra);
            int tongGia = thuePhongController.tinhTienThuePhong(thuePhong, soNgayThue);
            thuePhong.setTongGia(tongGia);

            if(thuePhongController.tiepNhanViecTraPhong(thuePhong))
            {
                JOptionPane.showMessageDialog(this.getComponent(0),"Trả phòng thành công!" , "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                initData();
            }
            else
            {
                JOptionPane.showMessageDialog(this.getComponent(0),"Trả phòng thất bại" , "Thông Báo", JOptionPane.ERROR_MESSAGE);
                initData();
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this.getComponent(0),ex.getMessage() , "Thông Báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBtnTraPhongMousePressed

    private void jCbMaPhongItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCbMaPhongItemStateChanged
        // TODO add your handling code here:
        if(jCbMaPhong.getItemCount()>0) {
            ThuePhong thuePhong = timThuePhongTheoMaPhong(Integer.parseInt(jCbMaPhong.getSelectedItem().toString()));
            hienThiThongTinThuePhong(thuePhong);

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

            if(sdf.format(thuePhong.getNgayThue()).equals(sdf.format(MyDateTime.getNow())))
            {
                jBtnHuyDatPhong.setEnabled(true);
            }
            else
            {
                jBtnHuyDatPhong.setEnabled(false);
            }
        }
}//GEN-LAST:event_jCbMaPhongItemStateChanged

    private void jBtnHuyDatPhongMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnHuyDatPhongMouseReleased
        // TODO add your handling code here:
        if(!jBtnHuyDatPhong.isEnabled())
            return;

        int result = JOptionPane.showConfirmDialog(this.getComponent(0), "Bạn có muốn hủy đặt phòng không","Thông Báo",JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(result == JOptionPane.OK_OPTION)
        {
            ThuePhong thuePhong = timThuePhongTheoMaPhong(Integer.parseInt(jCbMaPhong.getSelectedItem().toString()));
            if(thuePhongController.huyDatPhong(thuePhong.getId()))
            {
                JOptionPane.showMessageDialog(this.getComponent(0),"Hủy đặt phòng thành công" , "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                initData();
            }
            else
            {
                JOptionPane.showMessageDialog(this.getComponent(0),"Hủy đặt phòng thất bại" , "Thông Báo", JOptionPane.ERROR_MESSAGE);
                initData();
            }
        }
    }//GEN-LAST:event_jBtnHuyDatPhongMouseReleased


