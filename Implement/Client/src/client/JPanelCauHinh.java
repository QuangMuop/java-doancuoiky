    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        WorkerGetListLoaiKhachHang workerGetLoaiKhach = new WorkerGetListLoaiKhachHang();
        workerGetLoaiKhach.run();
        
        workerGetDSLoaiPhong.run();

        WorkerGetListThamSo workerGetThamSo = new WorkerGetListThamSo();
        workerGetThamSo.run();


        khachSan = khachSanController.layKhachSan();
        if(khachSan!=null)
        {
            hienThiThongTinKhachSan(khachSan);
        }

        try {
            lstLoaiKhachHang = workerGetLoaiKhach.get();
        } catch (InterruptedException ex) {
            Logger.getLogger(JPanelCauHinh.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExecutionException ex) {
            Logger.getLogger(JPanelCauHinh.class.getName()).log(Level.SEVERE, null, ex);
        }

        ArrayList<String> lstLoaiPhong = null;
        try {
            lstLoaiPhong = workerGetDSLoaiPhong.get();
        } catch (InterruptedException ex) {
            Logger.getLogger(JPanelCauHinh.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExecutionException ex) {
            Logger.getLogger(JPanelCauHinh.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            lstThamSo = workerGetThamSo.get();
        } catch (InterruptedException ex) {
            Logger.getLogger(JPanelCauHinh.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExecutionException ex) {
            Logger.getLogger(JPanelCauHinh.class.getName()).log(Level.SEVERE, null, ex);
        }

        if(lstLoaiKhachHang!=null)
        {
            initComboLoaiKhachHang();
        }

        if(lstLoaiPhong!=null)
        {
            this.lstGiaLoaiPhong = new ArrayList<Phong>();
            int i;
            for(i=0;i<lstLoaiPhong.size();i++)
            {
                Phong phong = null;
                String tenLoaiPhong = lstLoaiPhong.get(i);//.toLowerCase();
                if(MyCompare.compareString(tenLoaiPhong, "VIP") == 0)
                //if(tenLoaiPhong.equals("vip"))
                {
                    phong = new PhongVIP();
                }
                else
                {
                    if(MyCompare.compareString(tenLoaiPhong, "Hạng Trung") == 0)
                    //if(tenLoaiPhong.equals("hang trung"))
                    {
                        phong = new PhongHangTrung();
                    }
                    else
                        phong = new PhongHangThuong();
                }
                phong.setDonGiaPhong(phongController.getDonGiaPhongTheoTen(phong.getTenLoaiPhong()));
                this.lstGiaLoaiPhong.add(phong);
            }
            initComboLoaiPhong();            
        }

        if(lstThamSo!=null)
        {
            initComboThamSo();
        }
    }//GEN-LAST:event_formComponentShown

