    private void jBtnCapNhatMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnCapNhatMousePressed
        // TODO add your handling code here:
        if(!this.jBtnCapNhat.isEnabled())
            return;

        int result = JOptionPane.showConfirmDialog(this.getComponent(0),"Bạn có muốn lưu những thay đổi không?" , "Thông Báo", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(result == JOptionPane.OK_OPTION)
        {
            String s = jTxtGiaKhach.getText();
            if(s.equals(""))
            {
                JOptionPane.showMessageDialog(this.getComponent(0), "Nhập vào đơn giá khách hàng!","Thông Báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int giaKhach = Integer.parseInt(s);

            s = jTxtGiaPhong.getText();
            if(s.equals(""))
            {
                JOptionPane.showMessageDialog(this.getComponent(0), "Nhập vào đơn giá phòng!","Thông Báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            int giaPhong = Integer.parseInt(s);

            s = jTxtGiaTriThamSo.getText();
            if(s.equals(""))
            {
                JOptionPane.showMessageDialog(this.getComponent(0), "Nhập vào giá trị tham số!","Thông Báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int giaTriThamSo = Integer.parseInt(s);

            KhachHang loaiKhachHang = layLoaiKhachHangTheoTen(jCbLoaiKhach.getSelectedItem().toString());
            loaiKhachHang.setDonGiaKhachHang(giaKhach);
            
            Phong loaiPhong = layGiaLoaiPhongTheoTen(jCbLoaiPhong.getSelectedItem().toString());
            //loaiPhong.setGia(giaPhong);
            
            ThamSo thamSo = layThamSoTheoTen(jcbThamSo.getSelectedItem().toString());
            thamSo.setValue(giaTriThamSo);
            thamSo.setUse(jCheckThamSo.isSelected());

            if(!phongController.capNhatLoaiPhong(loaiPhong.getTenLoaiPhong(),giaPhong))
            {
                JOptionPane.showMessageDialog(this.getComponent(0),"Cập nhật gía tiền phòng thất bại" , "Thông Báo", JOptionPane.ERROR_MESSAGE);
            }else            
            if(!khachController.capNhatLoaiKhachHang(loaiKhachHang.getTenLoaiKhachHang(),loaiKhachHang.getDonGiaKhachHang()))
            {
                JOptionPane.showMessageDialog(this.getComponent(0),"Cập nhật gía tiền khách hàng thất bại" , "Thông Báo", JOptionPane.ERROR_MESSAGE);
            }else
                if(!thamSoController.capNhatThamSo(thamSo))
                {
                    JOptionPane.showMessageDialog(this.getComponent(0),"Cập nhật tham số" + thamSo.getName() + "thất bại" , "Thông Báo", JOptionPane.ERROR_MESSAGE);
                }
                else
                JOptionPane.showMessageDialog(this.getComponent(0),"Cập nhật thành công" , "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            //restore old values
            initComboLoaiKhachHang();
            initComboLoaiPhong();
            initComboThamSo();
        }
        
        //disable fields
        this.jBtnCapNhat.setEnabled(false);
        this.jTxtGiaKhach.setEditable(false);
        this.jTxtGiaPhong.setEditable(false);
        this.jTxtGiaTriThamSo.setEditable(false);
        this.jCheckThamSo.setEnabled(false);

        this.jBtnChinhSua.setEnabled(true);
    }//GEN-LAST:event_jBtnCapNhatMousePressed

