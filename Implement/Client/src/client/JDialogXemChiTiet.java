    private void jBtnChinhSua_XemChiTietMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnChinhSua_XemChiTietMousePressed
        // TODO add your handling code here:
        //show message "ban co muon chinh sua?";
       int returnValue = JOptionPane.showConfirmDialog(this.getComponent(0),"Ban co muon chinh sua noi dung phong hay khong?" , "Xac nhan", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
       if(returnValue == JOptionPane.OK_OPTION)
       {
            //enable fields
            jTxtLoaiPhong.setEditable(true);
            jTxtMoTa.setEditable(true);
            jTxtSoPhong.setEditable(true);

            //enable btnCapNhat
            jBtnCapNhat_XemChiTiet.setEnabled(true);
            jBtnChinhSua_XemChiTiet.setEnabled(false);
       }
    }//GEN-LAST:event_jBtnChinhSua_XemChiTietMousePressed

    private void jBtnCapNhat_XemChiTietMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnCapNhat_XemChiTietMousePressed
        // TODO add your handling code here:
        int returnValue = JOptionPane.showConfirmDialog(this.getComponent(0),"Ban co muon luu nhung thay doi hay khong?" , "Xac nhan", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
       if(returnValue == JOptionPane.OK_OPTION)
       {
           //disable fields
            jTxtLoaiPhong.setEditable(false);
            jTxtMoTa.setEditable(false);
            jTxtSoPhong.setEditable(false);

            //disable btnCapNhat
            jBtnCapNhat_XemChiTiet.setEnabled(false);
            jBtnChinhSua_XemChiTiet.setEnabled(true);

            //show message "Cap nhat thanh cong"
            JOptionPane.showMessageDialog(this.getComponent(0), "Cap nhat thong tin thanh cong","Information", JOptionPane.INFORMATION_MESSAGE);
       }
       else
       {
            //restore old value
       }
    }//GEN-LAST:event_jBtnCapNhat_XemChiTietMousePressed

